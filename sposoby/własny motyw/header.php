<strong>JACHTOWY STERNIK MORSKI </strong>
<p class="margin-training">Kurs na patent jachtowego sternika morskiego uprawnia do</p>

<ul class="ul-list-front" type="square">
 	<li>prowadzenia jachtów żaglowych po wodach śródlądowych</li>
 	<li>prowadzenia jachtów żaglowych o długości kadłuba do 18m po wodach morskich</li>
</ul>
<p class="margin-training">Wymagania do uzyskania patentu jachtowego sternika morskiego:</p>
<p class="margin-training">staż pełnomorski minimum 200h uzyskany w co najmniej dwóch rejsach i ukończone 18 lat oraz pozytywny wynik egzaminu na patent jachtowego sternika morskiego.</p>
<p class="margin-training">Informacje o formach szkolenia, dodatkowych opłatach oraz innych certyfikatach, które możesz zdobyć podczas kursu na jachtowego sternika morskiego znajdziesz w szczegółowym programie szkolenia<a href="http://tadekduch.pl/wp-content/uploads/2018/01/TD_2018_Szkolenie_na_jsm-min.pdf"><img src="/wp-content/uploads/2016/08/phoca_thumb_m_pdf.png" /></a></p>
<p class="margin-training"><b>PROGRAM "OD ZERA DO SKIPPERA"</b> &gt;&gt;&gt; czyli jak zdobyć patent jachtowego sternika morskiego w trzy tygodnie nie mając żadnego doświadczenia i stażu pełnomorskiego<a href="http://tadekduch.pl/wp-content/uploads/2018/01/TD_2018_Od_zera_do_skipera-min.pdf"><img src="/wp-content/uploads/2016/08/phoca_thumb_m_pdf.png" /></a></p>
